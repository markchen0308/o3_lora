/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	if(USART1==huart1.Instance)
	{	
		if(__HAL_UART_GET_FLAG(&huart1,UART_FLAG_IDLE))//if received idle
		{
			__HAL_UART_CLEAR_IDLEFLAG(&huart1);//clear idle flag
			HAL_UART_DMAStop(&huart1);//stop dma
			temp=__HAL_DMA_GET_COUNTER(&hdma_usart1_rx);//get dma remaind length
		  at_rec_str_st.at_rx_len_uart1=MAX_LEN_UART_BUF-temp; //get received length
			at_rec_str_st.flag_at_recv_uart1=true;//set received flag
		}
	}
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

